/**
 * Generates the pre-aligned demo-song data shipped with the landing page from
 * the committed Veil fixture, using the REAL alignment + EN-attach pipeline.
 * Only a short excerpt is baked (copyright: demonstration use, no audio bundled —
 * the app streams the song from YouTube). Furigana / readings / word-pairings are
 * NOT baked: the app enriches those live on song-open, which is itself part of
 * the showcase.
 *
 * Run: npx tsx scripts/build-demo-song.mjs
 * Output: src/landing/demoSong.data.json
 */
import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath, pathToFileURL } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
const root = join(here, '..')
const FIX = join(root, 'tests/ai-pipeline/fixtures/veil')
const OUT = join(root, 'src/landing/demoSong.data.json')

const EXCERPT_LINES = 12

async function main() {
  const { refineAlignmentWithPhrases } = await import(
    pathToFileURL(join(root, 'src/lyrics/phraseAlignment.ts')).href
  )
  const { smartAttachSecondLanguage } = await import(
    pathToFileURL(join(root, 'src/lyrics/lineAligner.ts')).href
  )
  const { embedTexts } = await import(pathToFileURL(join(root, 'scripts/lib/nodeEmbedder.mjs')).href)

  const ja = readFileSync(join(FIX, 'lyrics.ja.txt'), 'utf8')
    .split('\n').map((l) => l.trim()).filter(Boolean)
  const enBlock = readFileSync(join(FIX, 'lyrics.en.txt'), 'utf8').trim()
  const words = JSON.parse(readFileSync(join(FIX, 'transcript.words.json'), 'utf8'))
    .map((w) => ({ word: w.word, startTime: w.startTime, endTime: w.endTime }))

  // Align the full song for accuracy, then attach EN, then keep the excerpt.
  const refined = refineAlignmentWithPhrases(
    ja.map((original) => ({ original, translation: '', startTime: 0, endTime: 0 })),
    words,
    'ja',
  )
  const attached = (await smartAttachSecondLanguage(refined.lines, enBlock, embedTexts)).lines

  const excerpt = attached.slice(0, EXCERPT_LINES).map((l) => ({
    original: l.original,
    translation: l.translation ?? '',
    startTime: Number(l.startTime.toFixed(3)),
    endTime: Number(l.endTime.toFixed(3)),
  }))

  // Trim the transcript to the excerpt's span (+ small tail) so reading
  // reconciliation on open has the matching audio window without the whole song.
  const spanEnd = excerpt.at(-1).endTime + 3
  const transcriptWords = words
    .filter((w) => w.startTime < spanEnd)
    .map((w) => ({ word: w.word, startTime: Number(w.startTime.toFixed(3)), endTime: Number(w.endTime.toFixed(3)) }))

  const data = {
    _comment: 'Generated by scripts/build-demo-song.mjs from the Veil fixture. Do not edit by hand.',
    title: 'Veil (demo excerpt)',
    artist: 'Keina Suda',
    sourceLanguage: 'ja',
    translationLanguage: 'en',
    alignmentConfidence: Number(refined.confidence.toFixed(3)),
    lines: excerpt,
    transcriptWords,
  }
  writeFileSync(OUT, JSON.stringify(data, null, 2) + '\n')
  console.log(`Wrote ${OUT}: ${excerpt.length} lines, ${transcriptWords.length} transcript words, span 0–${spanEnd.toFixed(1)}s`)
}

main().catch((e) => { console.error(e); process.exit(1) })
